package polymorphismHw.java;

public class subtractOL {

    //HOMEWORK
    //OVERLOADING
    //CREATE A SUBTRACT METHOD W DIFFERENT PARAMETERS



    public int subtract(int a, int b){
        return a-b;
    }

    public int subtract(int a, int b, int c){
        return a-b-c;
    }

    public int subtract (int a, int b, int c, int d){
        return a-b-c-d;
    }
}
